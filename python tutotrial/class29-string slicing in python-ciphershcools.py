# slicing / sub sequences
a = "python"
# syntax - [start arguement : stop arguement -1]
print(a[0:4])
print(a[2:8])
print(a[2:])
print(a[:])
print(a[:8])
print(a[:3])
print(a[-3:6])
print(a[-1:])
print(a[-4:-6]) #shows space
print(a[-3:5])
print(a[2:-2])
print(a[2:-6]) #shows space
print(a[2:-1])
print(a[-3:6])